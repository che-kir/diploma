    private void ButtonOpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonOpenFileActionPerformed
        int returnVal = fileChooser.showOpenDialog(this);
    if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                File file = fileChooser.getSelectedFile();
                Workbook wb = Workbook.getWorkbook(file);
                Sheet s = wb.getSheet(0);
                int row = s.getRows();
                int col = s.getColumns();
                float D[] = new float[32];
                for(int i = 1; i < row; i++)
                    for(int j = 0; j < col; j++)
                    {
                        Cell c = s.getCell(j, i);
                        D[j] = Float.parseFloat(c.getContents().replace(',', '.'));
                    }
                for(int i = 0; i < 32; i++)
                    textfields[i].setText(String.valueOf(D[i]));
            } catch (IOException ex) {
                Logger.getLogger(KVVVreg.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BiffException ex) {
                Logger.getLogger(KVVVreg.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    } else {
        System.out.println("File access cancelled by user.");
    }
    }//GEN-LAST:event_ButtonOpenFileActionPerformed

